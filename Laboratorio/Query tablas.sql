create table USUARIO (
ID BIGINT NOT NULL PRIMARY KEY,
APELLIDOS VARCHAR(50) NOT NULL,
NOMBRES VARCHAR(50) NOT NULL,
FECHAREGISTRO DATE NOT NULL,
CLAVE VARCHAR(8) NOT NULL,
MAIL VARCHAR(20) NOT NULL,

)

create table FORMATO (
ID BIGINT NOT NULL PRIMARY KEY,
FORMATO VARCHAR(50) NOT NULL,

)


create table CATEGORIA (
ID BIGINT NOT NULL PRIMARY KEY,
CATEGORIA VARCHAR(50) NOT NULL,

)

create table REGISTRO (
ID BIGINT NOT NULL PRIMARY KEY,
IDUSUARIO BIGINT NOT NULL,
ISBN BIGINT NOT NULL,
FECHAADQUISICION DATE NOT NULL,
PRECIOADQUISICION BIGINT NOT NULL,
FECHAINICIOLECTURA DATE NOT NULL,
FECHAFINLECTURA DATE NOT NULL,
CALIFICACIONUSUARIO BIGINT NOT NULL,
IDFORMATO BIGINT NOT NULL FOREIGN KEY REFERENCES FORMATO (ID),
CONDICIONLIBRO BIGINT NOT NULL,
ESTADOLIBRO SMALLINT NOT NULL,


)

create table AUTOR (
ID INT NOT NULL PRIMARY KEY,
APELLIDOS VARCHAR(50) NOT NULL,
NOMBRES VARCHAR(50) NOT NULL,
FECHANAC DATE NOT NULL,
NACIONALIDAD VARCHAR(50) NOT NULL,

)


create table LIBRO (
ID SMALLINT NOT NULL PRIMARY KEY,
ISBN BIGINT NOT NULL,
ANIOPUBLICACION DATE NOT NULL,
CANTPAGINAS INT NOT NULL,
IDAUTOR INT FOREIGN KEY REFERENCES AUTOR (ID),
TITULO VARCHAR(50) NOT NULL,
IDCATEGORIA BIGINT NOT NULL FOREIGN KEY REFERENCES CATEGORIA (ID),

)
